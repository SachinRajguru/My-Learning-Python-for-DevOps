# Task 4: Assignment Operators

# Step 1: Initialize a variable
total = 10

# Step 2: Use assignment operators to update the value
total += 5   # Equivalent to total = total + 5 -> total becomes 15
total -= 3   # Equivalent to total = total - 3 -> total becomes 12
total *= 2   # Equivalent to total = total * 2 -> total becomes 24
total /= 4   # Equivalent to total = total / 4 -> total becomes 6.0

# Step 3: Print the final value
print("Final total:", total)  # Output: 6.0