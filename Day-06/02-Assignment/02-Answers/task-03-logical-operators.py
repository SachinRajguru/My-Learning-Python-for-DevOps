# Task 3: Logical Operators

# Step 1: Define two boolean variables
x = True
y = False

# Step 2: Perform logical operations
and_result = x and y    # True if both x and y are True
or_result = x or y      # True if at least one of x or y is True
not_result_x = not x    # Inverts the value of x (True becomes False)
not_result_y = not y    # Inverts the value of y (False becomes True)

# Step 3: Print the results
print("x and y:", and_result)   # Output: False
print("x or y:", or_result)     # Output: True
print("not x:", not_result_x)   # Output: False
print("not y:", not_result_y)   # Output: True