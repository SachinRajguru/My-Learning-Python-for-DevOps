# Calculator Functions (Linear Approach)

# Learning Note:
# The file `linear_calculator.py` shows how a calculator can be written in a single file.  
# This approach is **not recommended** for real projects, 
# but it helps in understanding the basics before moving to modular programming.

# --- Approach 1: Step-by-step (beginner friendly) ---
num1 = 10
num2 = 5

addition = num1 + num2
print("Value of addition: " + str(addition))

subtraction = num1 - num2
print("Value of subtraction: " + str(subtraction))

multiplication = num1 * num2
print("Value of multiplication: " + str(multiplication))

division = num1 / num2
print("Value of multiplication: " + str(division))


